<header><h1>Overview</header></h1>
<p>
Turkana County Government is one of the 47 county governments formed by the County Government’s Act in 2012 as prescribed in the Act and in the 2010 Constitution of Kenya. With its capital in Lodwar, the County Government of Turkana is a government for the people of Turkana and whose interests and aspirations are those for the people of Turkana.<br>
At 77,000 sq. km, Turkana County is the second largest county, covering more than 13% of Kenya’s surface. This vast land in the northwest of Kenya is an awakening economic giant, for beneath its surface lie huge oil deposits that are currently being explored and will see extraction in an industrial scale during the years to come – a windfall for a county with a population of slightly over 1 million.<br>
Moreover, Turkana boasts plenty of wind, sun, flowing water and underground steam for geothermal power which could make it literally a powerhouse in sustainable energies.<p>
